        if (selected < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete!");
            return;
        }
        
        Doctor doc = SystemAdmin.docDir.getDoctorList().get(selected);
        
        SystemAdmin.docDir.getDoctorList().remove(doc);
        SystemAdmin.docDir.getDoctorList().remove(doc);
        
        populateTable();
        
        ssnTxt.setText("");
        nameTxt.setText("");
        dobTxt.setText("");
        phoneTxt.setText("");
        emailTxt.setText("");
        houseTxt.setText("");
        zipTxt.setText("");
